package chap98_homework.nc230314;

public class _01_basic {

	public static void main(String[] args) {
		System.out.println("----------------------Basic 문제1----------------\n");
		int num = 10;
		System.out.println(++num); //출력: 11 값: 11
		System.out.println(++num); //출력: 12 값: 12
		System.out.println(num--); //출력: 12 값: 11
		System.out.println(--num); //출력: 10 값: 10
		System.out.println(num--); //출력: 10 값: 9
		System.out.println(--num); //출력: 8 값: 8

	}

}
